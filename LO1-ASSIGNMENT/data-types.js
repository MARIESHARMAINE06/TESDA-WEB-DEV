//Primitive Type

const name = 'Cardo Dalisay'; //string
console.log(name);

const age = 28; //integer
console.log(age);

const hungry = true; //boolean
console.log(hungry);

const money = null; //null
console.log(money);

let dogname; //undefined
console.log(dogname);

const favFood = ['adobo, sinigang, silog']; //reference type - object 
console.log(favFood);

